class Solution {
  public:
    int firstElement(int n) {
        // code here
        int a=1,b=1;
        int tmp=a,mod=1e9+7;
        for(int i=2;i<=n;i++){
            tmp=b;
            b=(b+a)%mod;
            a=tmp;
        }
        return tmp;
        
    }
};

